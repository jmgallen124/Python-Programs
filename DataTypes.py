# Program to demonstrate variables

# Declare variables
employee_name = "Jeremy"
employee_number = 7
is_employee = True
imaginary_number = 3+5j

# Print variables
print(employee_name)
print(employee_number)
print(is_employee)
print(imaginary_number)

# Change variables
employee_name = "Remy"
employee_number = 8

# Print again
print(employee_name)
print(employee_number)

# Print variable types
print(type(employee_name))
print(type(employee_number))
print(type(is_employee))
print(type(imaginary_number))

# Change employee number to float and print number and type
employee_number = 8.7
print(employee_number)
print(type(employee_number))